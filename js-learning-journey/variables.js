// variables are used to store values
// 3 ways to declare js variables
//1.var : (can be used anywehre (global_scope))
var a = 10;
var b = 30;
console.log(a + b);
//2.let : (used only within scope (local_scope))
let c = 10;
let d = 30;
console.log(c + d);
//3.const : (cannot be modified after declared)
const e = 10;
const f = 30;
console.log(e + f);

var str = "Sanjey";
console.log(str);
